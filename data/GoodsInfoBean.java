package data;

public class GoodsInfoBean {
	private String goCode;
	private String goName;
	private int goPrice;
	private String goComment;
	public String getGoCode() {
		return goCode;
	}
	public void setGoCode(String goCode) {
		this.goCode = goCode;
	}
	public String getGoName() {
		return goName;
	}
	public void setGoName(String goName) {
		this.goName = goName;
	}
	public int getGoPrice() {
		return goPrice;
	}
	public void setGoPrice(int goPrice) {
		this.goPrice = goPrice;
	}
	public String getGoComment() {
		return goComment;
	}
	public void setGoComment(String goComment) {
		this.goComment = goComment;
	}
	
	
}
